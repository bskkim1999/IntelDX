first = int( input("첫번째정수:") )
second = int( input("두번째정수:") )

print("{} + {} = {}".format( first, second, (first+second) ))
print("{} - {} = {}".format( first, second, (first-second) ))
print("{} / {} = {}".format( first, second, (first/second) ))
print("{} // {} = {}".format( first, second, (first//second) ))
print("{} % {} = {}".format( first, second, (first%second) ))
print("{} ** {} = {}".format( first, second, (first**second) ))

